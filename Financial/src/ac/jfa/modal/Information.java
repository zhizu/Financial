package ac.jfa.modal;

public class Information {

	private String phone_no;
	private String phone_key;
	private String ko_no;
	private String device_type;
	private String token;
	private String push_flag;
	private String pass_code;
	public String getPhone_no() {
		return phone_no;
	}
	public void setPhone_no(String phone_no) {
		this.phone_no = phone_no;
	}
	public String getPhone_key() {
		return phone_key;
	}
	public void setPhone_key(String phone_key) {
		this.phone_key = phone_key;
	}
	public String getKo_no() {
		return ko_no;
	}
	public void setKo_no(String ko_no) {
		this.ko_no = ko_no;
	}
	public String getDevice_type() {
		return device_type;
	}
	public void setDevice_type(String device_type) {
		this.device_type = device_type;
	}
	public String getToken() {
		return token;
	}
	public void setToken(String token) {
		this.token = token;
	}
	public String getPush_flag() {
		return push_flag;
	}
	public void setPush_flag(String push_flag) {
		this.push_flag = push_flag;
	}
	public String getPass_code() {
		return pass_code;
	}
	public void setPass_code(String pass_code) {
		this.pass_code = pass_code;
	}
	
}
